# 3. VO2max (Relative Ausdauer in mL/kg/min)
# ---------------------------------------------------------
def get_vo2max_reference(age, sex):
    """Berechnet P3 bis P97 basierend auf originalen SentrySuite LMS-Parametern."""
    if sex == 'boys':
        m = (-0.0015 * (age**3)) - (0.0321 * (age**2)) + (1.8851 * age) + 33.355
        l = 0.7198903
        s = (0.0004 * age) + 0.119
    else: # girls
        m = (-0.0025 * (age**3)) + (0.064 * (age**2)) - (0.1483 * age) + 37.968
        l = 0.7265509
        s = (0.005 * age) + 0.0713

    z_scores = {'P3': -1.88, 'P10': -1.28, 'P25': -0.675, 'P50': 0, 'P75': 0.675, 'P90': 1.28, 'P97': 1.88}
    result = {}
    for p, z in z_scores.items():
        if l == 0: result[p] = m * math.exp(s * z)
        else: result[p] = m * ((1 + l * s * z) ** (1/l))
    return result

