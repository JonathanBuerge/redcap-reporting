    data = {'girls': {}, 'boys': {}}
    z_scores = [-1.88, -1.28, -0.675, 0, 0.675, 1.28, 1.88]
    for age in range(6, 19):
        # Girls
        m_g = (14.904 * age) - 34.071
        l_g = -1.448041
        s_g = (0.0031 * age) + 0.1229
        data['girls'][age] = tuple(m_g * ((1 + l_g * s_g * z) ** (1/l_g)) for z in z_scores)
        # Boys
        m_b = (19.891 * age) - 75.024
