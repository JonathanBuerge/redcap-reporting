    a_high = min([k for k in ages if k >= a])

    if a_low == a_high:
        vals = np.array(data[a_low])
    else:
        w = (a - a_low) / (a_high - a_low)
        v_low = np.array(data[a_low])
        v_high = np.array(data[a_high])
        vals = v_low + w * (v_high - v_low)

    # Apply arm-swing correction before returning
